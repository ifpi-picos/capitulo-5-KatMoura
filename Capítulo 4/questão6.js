/*  As variáveis definidas como VAR, possuem escopo global ou de função, ou seja, podem ser utilizadas em 
qualquer lugar do código ou somente dentro de uma função, dependendo de onde ela for utilizada.
    As variáveis definidas como CONST são valores imutáveis, ou seja, que não podem ser mudadas no decorrer do
código. Além disso, precisam ser inicializadas no momento da sua declaração, diferente dos outros tipos.
    As variáveis definidas como LET são limitadas aos blocos onde foram declaradas. Diferente do VAR, não podem 
ser acessadas antes da declaração, evitando erros relacionados a hoisting. */ 


